void main() {
  int first = 10;
  int second = 3;

  /** Summation */
  print('$first + $second = ${first + second}');
  /** Subtraction */
  print('$first - $second = ${first - second}');
  /** Multiplication */
  print('$first * $second = ${first * second}');
  /** Division result Double */
  print('$first / $second = ${first / second}');
  /** Division result Integer */
  print('$first ~/ $second = ${first ~/ second}');
  /** Modulo */
  print('$first % $second = ${first % second}');
}
